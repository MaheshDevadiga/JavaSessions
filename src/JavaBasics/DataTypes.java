package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {
		//int char double boolean are primitive data types
		//string is a class

		//1.int
		int i=10;
		i=-56;
		System.out.println(i);
		//2. double - floating value
		double d=12.33;
		double d1=31.44;
		double d2=100;
		System.out.println(d+i);
		//3. character - only single digit value it must be within single quotes
		char c = 'a';
		char c1='A';
		char c2='1';
		char c3='$';
		
		//4. Boolean - Either 'True' or 'False'
		boolean b1=true;
		boolean b2=false;
		System.out.println(b1);
		
		//5. String is not a dataType it is a class
		String s = "Hello World";
		String s1 = "Selenium";
		String s2 = "Hi this is my java code";
		String s3 = "1000";
		String s4 = "22.23";
		
		
	}

}
